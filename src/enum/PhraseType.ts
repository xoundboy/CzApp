enum PhraseType {
    COLLOQUIALISM = 'colloquialism',
    IDIOM = 'idiom',
    MODALVERB = 'modal verb', // used in different tenses, eg 'you will have to' not 'you will must'
    OTHER = 'other',
    PHRASALVERB = 'phrasal verb', // with a preposition
    PROVERB = 'proverb',
    NONE = 'none'
}

export default PhraseType;